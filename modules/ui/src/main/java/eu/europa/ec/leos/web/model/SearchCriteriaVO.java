package eu.europa.ec.leos.web.model;

public class SearchCriteriaVO {
    private DocType type;
    private String year;
    private String number;

    public SearchCriteriaVO(DocType type, String year, String number) {
        super();
        this.type = type;
        this.year = year;
        this.number = number;
    }
    
    public DocType getType() {
        return type;
    }

    public void setType(DocType type) {
        this.type = type;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}
